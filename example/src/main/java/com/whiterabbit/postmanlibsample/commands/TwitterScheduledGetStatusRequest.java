public class TwitterScheduledGetStatusRequest implements RestServerRequest {
    private static final String url = "https://api.twitter.com/1.1/statuses/home_timeline.json";

    public TwitterScheduledGetStatusRequest(){

    }

    @Override
    public String getOAuthSigner() {
        return "Twitter";
    }

    @Override
    public String getUrl() {
        return url;
    }

    @Override
    public Verb getVerb() {
        return Verb.GET;
    }

    @Override
    public void onHttpResult(Response result, int statusCode, RequestExecutor executor, Context context) throws ResultParseException {
        if(statusCode == 200){
            ObjectMapper mapper = new ObjectMapper();
            try {
                JsonNode root = mapper.readTree(result.getBody());
                for (JsonNode tweet : root){
                    String text = tweet.path("text").toString();
                    String user = tweet.path("user").path("name").toString();
                    StoreUtils.setLatestTweet(String.format("%s -> %s", user, text), context);
                    Log.d("Twit", "Twit updated");
                }
            } catch (IOException e) {
                throw new ResultParseException("Failed to parse response");
            }
        }

    }

    @Override
    public void onHttpError(int statusCode, RequestExecutor executor, Context context) {
        Toast t = Toast.makeText(context, "Scheduled request Http Error", Toast.LENGTH_SHORT);
        t.show();
    }

    @Override
    public void onOAuthExceptionThrown(OAuthException exception) {
        //TODO Autogenerated
    }

    @Override
    public void addParamsToRequest(OAuthRequest request) {
        request.addQuerystringParameter("count", "1");
    }

    @Override
    public int describeContents() {
        return 0;  //TODO Autogenerated
    }

    @Override
    public void writeToParcel(Parcel parcel, int i) {
    }


    public static final Creator<TwitterScheduledGetStatusRequest> CREATOR
            = new Creator<TwitterScheduledGetStatusRequest>() {
        public TwitterScheduledGetStatusRequest createFromParcel(Parcel in) {
            return new TwitterScheduledGetStatusRequest(in);
        }

        public TwitterScheduledGetStatusRequest[] newArray(int size) {
            return new TwitterScheduledGetStatusRequest[size];
        }
    };


    public TwitterScheduledGetStatusRequest(Parcel in){
    }

}
